# Main
# ------------------------------------------------------------------
def main():
    print(f"Connecting to {DB_HOST}/{DB_NAME}...")
    conn = psycopg2.connect(
        host=DB_HOST, port=DB_PORT, dbname=DB_NAME,
        user=DB_USER, password=DB_PASSWORD,
        connect_timeout=10,
    )

    with conn.cursor(cursor_factory=psycopg2.extras.DictCursor) as cur:
        cur.execute("""
            SELECT id, website
            FROM lead
            WHERE website IS NOT NULL
              AND website <> ''
              AND website_live IS NULL
        """)
        leads = cur.fetchall()

    total = len(leads)
    print(f"Found {total} leads with a website.")
    print(f"Proxy: {PROXY_USER}@{PROXY_GATEWAY}")
    print(f"Workers: {WORKERS}  Batch size: {BATCH_SIZE}  Retries: {MAX_RETRIES}\n")

    live_count = 0
    dead_count = 0
    proxy_fallback_count = 0
    pending: list[tuple] = []

    with ThreadPoolExecutor(max_workers=WORKERS) as executor:
        futures = {executor.submit(check, row["id"], row["website"]): row for row in leads}

        for i, future in enumerate(as_completed(futures), 1):
            row = futures[future]
            try:
                lead_id, live, status_code, error = future.result()
            except Exception as e:
                lead_id, live, status_code, error = row["id"], False, None, str(e)

            via = ""
            if error == "proxy_failed_used_direct":
                proxy_fallback_count += 1
                via = " [direct fallback]"

            if live:
                live_count += 1
                print(f"  [{i}/{total}] LIVE  ({status_code}){via}  {row['website']}")
            else:
                dead_count += 1
                detail = f"status={status_code}" if status_code else (error or "unknown")
                print(f"  [{i}/{total}] DEAD  ({detail}){via}  {row['website']}")

            pending.append((lead_id, live, status_code, error))

            if len(pending) >= BATCH_SIZE:
                bulk_update(conn, pending)
                print(f"  -- flushed {len(pending)} records to DB --")
                pending.clear()

    if pending:
        bulk_update(conn, pending)
        print(f"  -- flushed {len(pending)} records to DB --")

    conn.close()
    print(f"\nDone.")
    print(f"  Live:           {live_count}")
    print(f"  Dead:           {dead_count}")
    print(f"  Proxy fallback: {proxy_fallback_count}")
    print(f"  Total:          {total}")


if __name__ == "__main__":
    main()
