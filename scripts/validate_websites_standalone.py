# ------------------------------------------------------------------
# Website check
# ------------------------------------------------------------------
def _is_security_wall(resp) -> bool:
    """True if a 403/503 comes from a security platform or geo-block — site is actually live."""
    server = resp.headers.get("server", "").lower()
    via = resp.headers.get("via", "").lower()

    # Known CDN/WAF in server or via header
    if any(sig in server for sig in SECURITY_SERVER_SIGNATURES):
        return True
    if any(sig in via for sig in SECURITY_SERVER_SIGNATURES):
        return True

    # Cloudflare-specific header
    if "cf-ray" in resp.headers:
        return True

    # CloudFront geo-block or access denied — check body snippet
    try:
        body = resp.text.lower()
        if any(sig in body for sig in GEO_BLOCK_BODY_SIGNATURES):
            return True
    except Exception:
        pass

    return False


def _is_live(resp) -> bool:
    """True if the response means the site is up."""
    code = resp.status_code
    if code < 400:
        return True
    if code in LIVE_DESPITE_ERROR_CODES:
        return True
    # 403 or 503 only counts as live if it's from a security platform
    if code in (403, 503) and _is_security_wall(resp):
        return True
    return False


def _do_request(url: str, proxies: dict) -> tuple[bool, int | None]:
    """Returns (is_live, status_code). Raises on connection error."""
    resp = requests.head(url, timeout=TIMEOUT, headers=HEADERS, allow_redirects=True, proxies=proxies)

    # HEAD not supported — retry as GET
    if resp.status_code in (405, 501):
        resp = requests.get(url, timeout=TIMEOUT, headers=HEADERS, allow_redirects=True, stream=True, proxies=proxies)
        resp.close()

    return _is_live(resp), resp.status_code


def check(lead_id: int, url: str) -> tuple:
    """Returns (lead_id, is_live, status_code, error).
    Retries proxy errors up to MAX_RETRIES times, then falls back to direct."""
    last_error = None

    # Try with proxy (with retries)
    for attempt in range(1, MAX_RETRIES + 2):  # +2: initial + retries
        try:
            live, code = _do_request(url, PROXIES)
            return lead_id, live, code, None
        except requests.exceptions.Timeout:
            return lead_id, False, None, "Timeout"
        except requests.exceptions.ProxyError as e:
            last_error = f"ProxyError (attempt {attempt}): {e}"
            if attempt <= MAX_RETRIES:
                time.sleep(RETRY_WAIT)
                continue
            break
        except requests.exceptions.ConnectionError as e:
            error_str = str(e)
            # If the connection error is about the proxy itself, retry
            if "ProxyError" in error_str or "37.48.118.90" in error_str:
                last_error = f"ProxyConnectionError (attempt {attempt})"
                if attempt <= MAX_RETRIES:
                    time.sleep(RETRY_WAIT)
                    continue
                break
            # Otherwise it's the target site that's down — no point retrying
            return lead_id, False, None, f"ConnectionError: {e}"
        except Exception as e:
            return lead_id, False, None, str(e)

    # Proxy failed after all retries — fall back to direct connection
    try:
        live, code = _do_request(url, proxies={})
        return lead_id, live, code, f"proxy_failed_used_direct"
    except requests.exceptions.Timeout:
        return lead_id, False, None, f"Timeout (direct, proxy_failed)"
    except Exception as e:
        return lead_id, False, None, f"proxy_failed + direct_error: {e}"


# ------------------------------------------------------------------
# Bulk DB update
# ------------------------------------------------------------------
def bulk_update(conn, batch: list[tuple]):
    live_ids = [r[0] for r in batch if r[1]]
    dead_ids = [r[0] for r in batch if not r[1]]
    with conn.cursor() as cur:
        if live_ids:
            cur.execute(
                "UPDATE lead SET website_live = TRUE WHERE id = ANY(%s)",
                (live_ids,)
            )
        if dead_ids:
            cur.execute(
                "UPDATE lead SET website_live = FALSE WHERE id = ANY(%s)",
                (dead_ids,)
            )
    conn.commit()


